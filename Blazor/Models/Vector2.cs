class Vector2
{
    public double _X;
    public double _Y;
    public double Height
    {
        get { return _X; }
        set { _X = value; }
    }
    public double Width
    {
        get { return _Y; }
        set { _Y = value; }
    }

    public double Y
    {
        get { return _Y; }
        set { _Y = value; }
    }

    public double X
    {
        get { return _X; }
        set { _X = value; }
    }

    public double Latitude
    {
        get { return _X; }
        set { _X = value; }
    }
    public double Longitude
    {
        get { return _Y; }
        set { _Y = value; }
    }
}